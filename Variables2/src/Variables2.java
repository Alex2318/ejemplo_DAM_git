
public class Variables2 {

	public static void main(String[] args) {
		//Variables
		//tipo nombreVariable=valor;
		int numeroEntero=4;
		int numeroSinDefinir=0;
		
		//Numerós decimales 
		float decimal=12.5f;
		double decimalMasLargo=12.5d;
		
		//Tipo CHAR
		char letra='A';
		String letraString="a";
		
		//Tipo BOOLEAN
		boolean semaforo=true;
		
		//Imprimir números enteros
		System.out.println(numeroEntero);
		System.out.println(numeroSinDefinir);

		//Imprimir números decimales
		System.out.println(decimal);
		System.out.println(decimalMasLargo);
		
		//Imprimir CHAR
		System.out.println(letra);
		System.out.println(letraString);
		
		//Imprimir BOOLEAN
		System.out.println(semaforo);

	}

}
